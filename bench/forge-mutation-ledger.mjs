#!/usr/bin/env node
// bench/forge-mutation-ledger.mjs
// Mutation-backed admit ledger (item 1): MEASURE that mutationAdmit strengthens admission past admitCheck's
// "fails the ONE observed bad" to "kills an oracle-confirmed mutant NEIGHBOURHOOD" — WITHOUT falsely rejecting
// a generalizing check. Deterministic, $0 (no model spend): real mutate + real scorerRunCheck on stateful
// scenarios. Per scenario, with an independent operator oracle (a held-out io-trace spec), we compare on:
//   WEAK check   — passes good, fails the observed bad (admitCheck ADMITS) but is pointwise-overfit:
//                  admitCheck.admit = true  AND  mutationAdmit.admit = FALSE  (kills < threshold of neighbourhood)
//   STRONG check — generalizes: admitCheck.admit = true  AND  mutationAdmit.admit = TRUE (kills >= threshold)
//   non-brittle  — the STRONG check is still admitted when good = an ALTERNATE honest impl (no false rejection)
// The mutant neighbourhood is generated by the REAL textual mutator; oracle-confirmed-bad mutants are the
// denominator, the candidate's CLEAN kills the numerator (a crash is NOT a kill — codex finding 3).
//
// Run: node bench/forge-mutation-ledger.mjs           (always $0)
//      node bench/forge-mutation-ledger.mjs --verify  (terse; exit 1 if the ledger regresses)
import { mkdtempSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { scorerRunCheck, admitCheck } from '../src/forge/admit.mjs'
import { mutationAdmit } from '../src/forge/mutation-admit.mjs'
import { shq } from '../src/shq.mjs'

const HERE = dirname(fileURLToPath(import.meta.url))
const IO_TRACE = join(HERE, '..', 'scorers', 'io-trace.mjs')
const VERIFY = process.argv.includes('--verify')
const writeSrc = (src) => { const p = join(mkdtempSync(join(tmpdir(), 'mut-led-')), 'impl.mjs'); writeFileSync(p, src); return p }
const traceCmd = (sub, trace, expect) => `node ${shq(IO_TRACE)} ${sub} --trace ${shq(trace)} --expect ${shq(expect)}`

const SCENARIOS = [
  {
    id: 'counter',
    sub: '--factory makeCounter',
    honest: 'export const makeCounter = () => { let n = 0; return { inc() { return ++n }, value() { return n } } }\n',
    gamed: 'export const makeCounter = () => ({ inc() {}, value() { return 1 } })\n',
    altHonest: 'export const makeCounter = () => { const c = { n: 0 }; return { inc() { c.n += 1; return c.n }, value() { return c.n } } }\n',
    oracle: ['[["inc"],["value"],["inc"],["value"]]', '[1,1,2,2]'],
    weak: ['[["value"]]', '[0]'],          // fresh value()===0: passes good, fails constant-1 bad, but MISSES increment-no-op
    strong: ['[["inc"],["value"]]', '[1,1]'], // inc returns 1 then value 1: kills the whole neighbourhood
  },
  {
    id: 'toggle',
    sub: '--factory makeToggle',
    honest: 'export const makeToggle = () => { let on = false; return { toggle() { on = !on; return on }, state() { return on } } }\n',
    gamed: 'export const makeToggle = () => ({ toggle() { return true }, state() { return true } })\n',
    altHonest: 'export const makeToggle = () => { let c = 0; return { toggle() { c += 1; return c % 2 === 1 }, state() { return c % 2 === 1 } } }\n',
    oracle: ['[["toggle"],["state"],["toggle"],["state"]]', '[true,true,false,false]'],
    weak: ['[["state"]]', '[false]'],        // fresh state()===false: passes good, fails the always-true bad, but MISSES toggle behaviour
    strong: ['[["toggle"],["toggle"],["state"]]', '[true,false,false]'], // double-toggle returns to false: the always-true gamed bad fails this
  },
]

const MIN_CONFIRMED = 1 // mechanism ledger: strengthen even on a small neighbourhood (production default is 2)
async function evalScenario(sc) {
  const good = writeSrc(sc.honest), bad = writeSrc(sc.gamed), alt = writeSrc(sc.altHonest)
  const oracle = traceCmd(sc.sub, ...sc.oracle)
  const weak = traceCmd(sc.sub, ...sc.weak)
  const strong = traceCmd(sc.sub, ...sc.strong)
  const baseAdmit = (cmd, g, b) => admitCheck({ candidateCmd: cmd, goodArtifact: g, badArtifact: b, replayRuns: 2, runCheck: scorerRunCheck })
  const mutAdmit = (cmd, g, b) => mutationAdmit({ candidateCmd: cmd, goodArtifact: g, badArtifact: b, replayRuns: 2, runCheck: scorerRunCheck, oracleCmds: [oracle], minConfirmedMutants: MIN_CONFIRMED })
  const weakBase = await baseAdmit(weak, good, bad)
  const weakMut = await mutAdmit(weak, good, bad)
  const strongBase = await baseAdmit(strong, good, bad)
  const strongMut = await mutAdmit(strong, good, bad)
  const strongAlt = await mutAdmit(strong, alt, bad) // non-brittle: strong still admitted on an ALTERNATE honest impl
  return {
    id: sc.id,
    weakBaseAdmit: weakBase.admit, weakMutAdmit: weakMut.admit, weakMutation: weakMut.mutation,
    strongBaseAdmit: strongBase.admit, strongMutAdmit: strongMut.admit, strongMutation: strongMut.mutation,
    strongAltAdmit: strongAlt.admit,
    // the headline: admitCheck would let the weak check in; mutationAdmit catches the overfit
    weakStrengthened: weakBase.admit === true && weakMut.admit === false,
    strongPreserved: strongBase.admit === true && strongMut.admit === true,
    nonBrittle: strongAlt.admit === true,
  }
}

const rows = []
for (const sc of SCENARIOS) rows.push(await evalScenario(sc))

const n = rows.length
const strengthened = rows.filter((r) => r.weakStrengthened).length
const preserved = rows.filter((r) => r.strongPreserved).length
const nonBrittle = rows.filter((r) => r.nonBrittle).length
const ok = strengthened === n && preserved === n && nonBrittle === n

if (!VERIFY) {
  console.log('\n=== Forge mutation-backed admit ledger (item 1) — deterministic, $0 ===\n')
  console.log('mutationAdmit ⊑ admitCheck: it calls the base gate first and only ADDS a rejection (kills < threshold of\nthe oracle-confirmed mutant neighbourhood). The WEAK check passes good and fails the one observed bad — admitCheck\nadmits it — but misses sibling mutants; mutationAdmit rejects it. The STRONG check kills the neighbourhood.\n')
  for (const r of rows) {
    const wm = r.weakMutation, sm = r.strongMutation
    console.log(`[${r.id}]`)
    console.log(`  WEAK:   admitCheck=${r.weakBaseAdmit ? 'ADMIT' : 'reject'}  mutationAdmit=${r.weakMutAdmit ? 'admit' : 'REJECT'}  (killed ${wm?.killed}/${wm?.confirmedMutants} confirmed, ${wm?.crashed} crash, ${wm?.excluded} excluded)  ${r.weakStrengthened ? '✓ overfit caught' : '✗'}`)
    console.log(`  STRONG: admitCheck=${r.strongBaseAdmit ? 'ADMIT' : 'reject'}  mutationAdmit=${r.strongMutAdmit ? 'ADMIT' : 'reject'}  (killed ${sm?.killed}/${sm?.confirmedMutants} confirmed)  ${r.strongPreserved ? '✓ preserved' : '✗'}   non-brittle(alt honest)=${r.nonBrittle ? '✓' : '✗'}`)
  }
  console.log('\n=== aggregate ===')
  console.log(`weak overfit caught:  ${strengthened}/${n} (admitCheck admits, mutationAdmit rejects)`)
  console.log(`strong preserved:     ${preserved}/${n} (admitted by BOTH — no false rejection of a generalizing check)`)
  console.log(`non-brittle:          ${nonBrittle}/${n} (strong still admitted when good = an alternate honest impl)`)
  console.log(
    ok
      ? `\nreading: NON-NULL — mutationAdmit strengthens admission on ${strengthened}/${n} pointwise-overfit checks the base gate\nwould have admitted, while preserving ${preserved}/${n} generalizing checks (and non-brittle on alternate honest impls).\nThe equivalent-mutant defense (oracle-filter: a non-confirmed mutant is never a required-kill) is unit-proven in\ntest/forge-mutation-admit.test.mjs. Honest limit: the kill-ratio threshold is a heuristic dial, not a generalization\nproof (codex finding 4); ratios are reported, not hidden.`
      : `\nreading: UNEXPECTED — strengthened=${strengthened}/${n} preserved=${preserved}/${n} nonBrittle=${nonBrittle}/${n}. Investigate before claiming item 1.`,
  )
} else {
  console.log(ok ? `mutation ledger OK: overfit-caught=${strengthened}/${n} strong-preserved=${preserved}/${n} non-brittle=${nonBrittle}/${n}` : `mutation ledger FAIL: overfit=${strengthened}/${n} preserved=${preserved}/${n} nonBrittle=${nonBrittle}/${n}`)
}

const reports = join(HERE, 'reports')
if (!existsSync(reports)) mkdirSync(reports, { recursive: true })
writeFileSync(join(reports, 'forge-mutation-ledger.jsonl'), rows.map((r) => JSON.stringify(r)).join('\n') + '\n')
process.exit(ok ? 0 : 1)
